public class Operacion {
    
public String Mes(int mes)
{
switch (mes)
{
    case 1:
        return "Enero";
    case 2:
        return "febrero";
    case 3:
        return "marzo";
    case 4:
        return "abril";
    case 5:
        return "mayo";
    case 6:
        return "junio";
    case 7:
        return "julio";
    case 8:
        return "agosto";
    case 9:
        return "setiembe";
    case 10:
        return "octubre";
    case 11:
        return "noviembre";
    case 12:
        return "diciembre";
        
    default:
        return "";

}

}      
  

}
